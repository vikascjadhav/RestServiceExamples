package org.app.mrmobile.dtos;

public class MrResponse {
	String inputNamename;
	String outputNamr;
	String address;
	public String getInputNamename() {
		return inputNamename;
	}
	public void setInputNamename(String inputNamename) {
		this.inputNamename = inputNamename;
	}
	public String getOutputNamr() {
		return outputNamr;
	}
	public void setOutputNamr(String outputNamr) {
		this.outputNamr = outputNamr;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
}
